#ifndef CONVERTER_OPS_H
#define CONVERTER_OPS_H

// ================= TEMPERATURE CONVERSIONS =================

// Celsius to Fahrenheit
void c_to_f();

// Fahrenheit to Celsius
void f_to_c();


// ================= LENGTH CONVERSIONS =================

// Kilometer to Meter
void km_to_m();

// Kilometer to Centimeter
void km_to_cm();

// Meter to Kilometer
void m_to_km();

// Meter to Centimeter
void m_to_cm();

// Centimeter to Meter
void cm_to_m();

// Inch to Centimeter
void inch_to_cm();

// Centimeter to Inch
void cm_to_inch();

// Kilometer to Mile
void km_to_mile();

// Mile to Kilometer
void mile_to_km();


// ================= MASS CONVERSIONS =================

// Gram to Kilogram
void g_to_kg();

// Kilogram to Gram
void kg_to_g();

// Gram to Pound
void g_to_pound();

// Pound to Gram
void pound_to_g();


// ================= VOLUME CONVERSIONS =================

// Litre to Millilitre
void l_to_ml();

// Millilitre to Litre
void ml_to_l();


// ================= NUMBER SYSTEM CONVERSIONS =================

// Decimal to Binary
void dec_to_binary();

// Decimal to Octal
void dec_to_octal();

// Decimal to Hexadecimal
void dec_to_hex();

#endif
